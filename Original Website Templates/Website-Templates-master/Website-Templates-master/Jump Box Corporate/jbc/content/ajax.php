  <h2 class="content-heading">jBox.Corporate Ajax Features</h2>
  <p>JBC is the totally Ajax based template. But as you probably know Ajax have some of disadvantages such as SEO compatibility and problems with page bookmarking, not working browser navigation buttons, etc. But JBC uses very <strong>smart Ajax</strong> which is comes without mentioned above problems!</p>
  <h3 class="inner-heading">SEO Friendly Ajax</h3>
  <p>JBC powered with SEO Friendly Ajax engine due to the use of special inner linking. So all your pages will be indexed by Search Engines (except popups)! JBC also comes with special <strong>SEO script</strong>, which allows you to specify title, meta keywords and meta description for all of your pages!</p>
  <h3 class="inner-heading">User Friendly Ajax</h3>
  <p>As you know in the Ajax websites browser's URL do not changing after you load some page, so when you click on the "Refresh" browser's button you will back to the firs page (mostly at home page), the same thing with browser's "Back" button, or you can not share with a link to the new page. All these is not good for your users, but JBC has not these disadvantages due to the use of special redirect scripts and couple of secrets:). So ... </p>
  <ul>
    <li>
      <p>you can go to the <a href="?page=typography" class="ajax">Typography</a> page and then try to click on the browser's <strong>"Back"</strong> button and you will see the "inverse" animation to this page!</p>
    </li>
    <li>
      <p>you can go to the <a href="?page=typography" class="ajax">Typography</a> page and then try to click on the browser's <strong>"Refresh"</strong> button and after refresh you will see the Typography page, not this or home page!</p>
    </li>
    <li>
      <p>you can go to the <a href="?page=typography" class="ajax">Typography</a> page and then copy the browser's URL to clipboard, open new window (or new browser tab) and past there this URL, and the Typography page will be opened! This allows your visitors to share with a links to the loaded page and use <strong>social bookmarking services</strong> on your pages!</p>
    </li>
  </ul>
  <h3 class="inner-heading">Fool Protection</h3>
  <p>What will happen if your visitor will click on some link while animation process between pages is not finished, or what will happen if he click on the link to the page which is currently "active"? Nothing! JBC comes with "Fool Protection":</p>
  <ul>
    <li>
      <p>On the time of page animation all Ajax links will be disabled! So if your visitor will click some inner "Ajax" link, nothing will happen!</p>
    </li>
    <li>
      <p>If you have links on the page which are links to this "active" page, they will be automatically disabled! Such kind of links usually can be founded in sidebar and footer modules.</p>
    </li>
  </ul>
  <h3 class="inner-heading">Disabled Java Script?</h3>
  <p>Don't worry JBC is ready for visitors with disabled Java Scripts! All pages and all content will be still accessible but without animation. Even submenus will work fine!</p>
  <h3 class="inner-heading">Do not like Ajax and Animation?</h3>
  <p>No problems, you can disable all Ajax by changing one variable in template's configuration file from <strong>"disableAjax : <em>false</em>"</strong> to <strong>" disableAjax : <em>true</em>"</strong> !</p>
