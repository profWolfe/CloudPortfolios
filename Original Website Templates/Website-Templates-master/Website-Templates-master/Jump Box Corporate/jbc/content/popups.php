  <h2 class="content-heading">jBox.Corporate Amazing Popups</h2>
  <p>JBC comes with really amazing popup windows! There are two different type of Popup window:</p>
  <h3 class="inner-heading">1. Rotate Popup</h3>
  <p>This kind of popup window is the fast and simple way to open a Popup window with some kind of rotate animation. It allows you to open popup window with HTML content inside (by Ajax) and popup window with image. For window with content you can set required window width by adding the special attribute to your link, otherwise it will be opened with a default width (600px). Examples:</p>
  <ul>
    <li>Rotate popup with <strong>content</strong> and default width: <a class="popup" href="folio/folio1.php">Open Popup</a></li>
    <li>Rotate popup with <strong>content</strong> and 1000px in width: <a class="popup" data-width="1000" href="folio/folio1.php">Open Popup</a></li>
    <li>Rotate popup with <strong>content</strong> and 200px in width: <a class="popup" data-width="200" href="folio/folio2.php">Open Popup</a></li>
    <li>Rotate popup with <strong>width image</strong>: <a class="pop-image" href="images/demo/1.jpg">Open Popup</a></li>
    <li>Rotate popup with <strong>narrow image</strong>: <a class="pop-image" href="images/demo/5.jpg">Open Popup</a></li>
  </ul>
